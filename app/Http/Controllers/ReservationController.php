    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->validate([
            'name' => 'required|string',
            'organization_name' => 'required|string',
            'address' => 'required|string',
            'email' => 'required|string',
            'phone' => 'required|numeric',
            'reservefrom' => 'required|date',
            'reserveto' => 'required|date',
            'hall' => 'required|string',
            'trainee' => 'required|numeric',
            'male' => 'required|numeric',
            'female' => 'required|numeric',
            'loding' => 'required|numeric',
            'fooding' => 'required|numeric',
            'type' => 'required|string',
        ]);

        Reservation::create($data);

        return redirect(route('reservation'))->with('success', 'Reservation made sucessfully');
    }

